#!/bin/bash
$exit_p=0
while [ $exit_p=0 ]
do
    echo "==============================================================================="
    echo "||                                     Menu                                  ||"
    echo "==============================================================================="
    echo "|| 1. Show the list of employees                                             ||"
    echo "|| 2. Add new empoloyee                                                      ||"
    echo "|| 3. Exit                                                                   ||"
    echo "==============================================================================="
    echo -n ":: "
    read option
    case $option in

    1)
        clear
        echo "==============================================================================="
        echo "||                                 Employees list                            ||"
        echo "==============================================================================="
        echo "~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~"
        cat $1
        echo ""
        echo "~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~"
        echo ""
        echo ""
    ;;

    2)
        ./add_new.sh $1
    ;;

    0)
        $exit_p=1
    ;;

    *)
        echo "                        ***invalid input. try again***"
    ;;
    esac
done

